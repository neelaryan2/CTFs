a = [[a[n * i + j] for j in range(n)] for i in range(n)]

ans = []
def solve(prev_row, remaining, left):
	global ans
	assert left >= 0
	if not remaining and prev_row == 0:
		return True
	b = a[prev_row]
	cur = sorted(range(n), key=lambda x: b[x])
	for i in cur:
		if b[i] > left:
			return False
		removed = i in remaining
		if removed:
			remaining.remove(i)
		if solve(i, remaining, left - b[i]):
			ans.append(i)
			return True
		if removed:
			remaining.add(i)
	return False


